public class Person {
	/** 社区 */
	public String sq;
	/** 名称 */
	public String mc;

	public String getSq() {
		return sq;
	}

	public void setSq(String sq) {
		this.sq = sq;
	}

	public String getMc() {
		return mc;
	}

	public void setMc(String mc) {
		this.mc = mc;
	}

	public Person(String sq, String mc) {
		this.sq = sq;
		this.mc = mc;
	}

}
